    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String telefone = txtTelefone.getText();
        String data = txtData.getText();
        boolean jaFoiPaciente = cbPaciente.isSelected();
        
        if(!nome.isEmpty() && !cpf.isEmpty() && !telefone.isEmpty() && !data.isEmpty()){
            if(cpf.matches("\\d+")){
                if(telefone.matches("\\d+")){
                    if(data.matches("\\d+")){
                        Paciente novoPaciente = new Paciente(nome, cpf, telefone, data, jaFoiPaciente);
                        principal2.adicionarPaciente(novoPaciente);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(this, "o campo data deve ter apenas números.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "o campo telefone deve ter apenas números.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "o campo CPF deve ter apenas números.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.", "Erro de Validação", JOptionPane.WARNING_MESSAGE);
        }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro de validação.", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

